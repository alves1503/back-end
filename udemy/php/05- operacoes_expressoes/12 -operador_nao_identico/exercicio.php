$y  = 3;

//Igual
if ($x == $y) {
    echo "É igual";
} else {
    echo "Não igual";
}

echo "<br>";

// Diferente
if ($x != $y) {
    echo "É diferente";
} else {
    echo "Não diferente";
}


echo "<br>";


// Idêntico
if ($x === $y) {
    echo "É identico";
} else {
    echo "Não é identico";
}

echo "<br>";

// Não identico
if ($x !== $y) {
    echo "Não é identico  2";
} else {
    echo "É identico";
